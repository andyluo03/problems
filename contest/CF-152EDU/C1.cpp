int add_r (int a, int b){return a+b;}

const int inf = 0;
struct Node {
	Node *l = 0, *r = 0;
	int lo, hi, mset = inf, madd = 0, val = 0;
	Node(int lo,int hi):lo(lo),hi(hi){} // Large interval of -inf
	Node(vi& v, int lo, int hi) : lo(lo), hi(hi) {
		if (lo + 1 < hi) {
			int mid = lo + (hi - lo)/2;
			l = new Node(v, lo, mid); r = new Node(v, mid, hi);
			val = add_r(l->val, r->val);
		}
		else val = v[lo];
	}
	int query(int L, int R) {
		if (R <= lo || hi <= L) return -inf;
		if (L <= lo && hi <= R) return val;
		push();
		return add_r(l->query(L, R), r->query(L, R));
	}
	void set(int L, int R, int x) {
		if (R <= lo || hi <= L) return;
		if (L <= lo && hi <= R) mset = val = x, madd = 0;
		else {
			push(), l->set(L, R, x), r->set(L, R, x);
			val = add_r(l->val, r->val);
		}
	}
	void add(int L, int R, int x) {
		if (R <= lo || hi <= L) return;
		if (L <= lo && hi <= R) {
			if (mset != inf) mset += x;
			else madd += x;
			val += x;
		}
		else {
			push(), l->add(L, R, x), r->add(L, R, x);
			val = add_r(l->val, r->val);
		}
	}
	void push() {
		if (!l) {
			int mid = lo + (hi - lo)/2;
			l = new Node(lo, mid); r = new Node(mid, hi);
		}
		if (mset != inf)
			l->set(lo,hi,mset), r->set(lo,hi,mset), mset = inf;
		else if (madd)
			l->add(lo,hi,madd), r->add(lo,hi,madd), madd = 0;
	}
};

void solution(){
    int n, m; cin >> n >> m;
    vector<int> a(n);
    string t; cin >> t;
    forn(n){a[i] = t[i]-'0';}

    Node root(a, 0, a.size()-1);

    int ans = 0;
    forn(m){
        int l, r; cin >> l >> r;
        l--;
        r--;

        int cnt = root.query(l, r);
        cout << l << " " << r << " " << cnt << endl;
        if(cnt == 0 || cnt == (l-r)+1)
            continue;
        if(root.query(l, r-(cnt-1)) == 0)
            continue;
        cout << '!' << endl;
        ans++;
        root.set(l, r-(cnt), 0);
        root.set(r-cnt+1, r, 1);
    }

    cout << ans;
}

